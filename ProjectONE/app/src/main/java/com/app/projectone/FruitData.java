package com.app.projectone;

public class FruitData {

    int f_image;
    String f_name, f_details;

    public FruitData() {
    }

    public int getF_image() {
        return f_image;
    }

    public String getF_name() {
        return f_name;
    }

    public String getF_details() {
        return f_details;
    }

    public void setF_image(int f_image) {
        this.f_image = f_image;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public void setF_details(String f_details) {
        this.f_details = f_details;
    }
}
